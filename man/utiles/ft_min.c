#include "../push_swap.h"

int	min(t_list *stack)
{
	int		m;
	t_list	*tmp;

	tmp = stack;
	m = 2147483647;
	while (tmp)
	{
		if (tmp->data < m)
		{
			m = tmp->data;
		}
		tmp = tmp->next;
	}
	return (m);
}
